#using inputs, perform the logic, return output.

def add(num1,num2):
    add = num1 + num2
    return(add)

def sub(num1,num2):
    sub = num1 - num2
    return(sub)

def mul(num1,num2):
    mul  = num1 * num2
    return(mul)
def div(num1,num2):
    div = num1 / num2
    return(div)


print(add(10,20))
print(sub(30,80))
print(mul(25,35))
print(div(2,8))